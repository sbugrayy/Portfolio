/** language === 'en' ise obj[key + '_en'] döner (varsa), yoksa obj[key] */
export function pick(obj, key, lang) {
  const enVal = obj[`${key}_en`];
  return (lang === 'en' && enVal != null) ? enVal : obj[key];
}

